<!-- =========================
     ADOPTION PROCESS
============================== -->
	<div class="container">
		<div>
			<header class="section-header">
				<h2 class="title-text">ADOPTION PROCESS</h2><br>
			</header>			
			<p class="sub-title"><ul>
							<li>
It is our sincere wish to match each of the pets in our program with families that are perfect for them. We want our animals and our adopters to be happy with each other forever. We try to get to know potential adopters and counsel them to help them find the perfect pet for their family. The following is an overview of our adoption process: </a></li><br><br>
							<li><h5 class="title-text text-capitalize">1.Meeting with the pet</h5></li><br>
							<li>The potential adopter needs to meet the pet in person to see if they are truly interested in adopting. This can take place at one of our adoption events, at the foster parent's house, or at the potential adopter's house. Just give us a call to set up a meeting. </a></li><br><br>
							<li><h5 class="title-text text-capitalize">2.Adoption Application </h5></li><br>
							<li>The potential adopter must complete our adoption application. This provides us with basic information about the household that is helpful in determining whether or not the animal will be a good match. </a></li><br><br>
							<li><h5 class="title-text text-capitalize">3.Screening</h5></li><br>
							<li>Once we have received the application, the Adoption service Executive screens the application and does the reference checks. </a></li><br><br>
							<li><h5 class="title-text text-capitalize">4.Home Visit</h5></li><br>
							<li>After the application has been reviewed and approved, a home visit is scheduled. This allows the applicant, the fosterer and the Adoption service Executive to see how the pet reacts to the potential new home and how they interact with all members of the family. </a></li><br><br>
							<li><h5 class="title-text text-capitalize">5.Approval and Transfer</h5></li><br>
							<li>If all parties (applicant, fosterer and Adoption service Executive ) are in agreement that it seems like a good match, an extended visit (full day or overnight) is scheduled before the adoption is finalized. An adoption agreement is signed by the applicant. </a></li><br><br>
							<li><h5 class="title-text text-capitalize">6.Follow-up</h5></li><br>
							<li>Follow-up calls to the adopters are done to make sure that things are going smoothly and to assist with any problems or concerns. Referrals are made to our behavior counselor if needed. If at any time a placement does not work out, for whatever reason, the pet MUST BE RETURNED to SCAN Foundation within 10 days</a></li><br><br>
							<li><h5 class="title-text text-capitalize">7.Animal Behavior Clinic</h5></li><br>
							<li>The Animal Behavior Clinic functions to help adopters solve behavior problems of their companion animals. Common problems are aggression or destructiveness by dogs and house soiling or aggression in cats. This is free for all adopted pets. </a></li><br><br>
							<li><h5 class="title-text text-capitalize">8.Veterinary Medical Services</h5></li><br>
							<li>A roster of experienced, licensed veterinarians and licensed veterinary technicians provide quality, low cost vet services to adopted pets for fist year. Our staff veterinarians provide high quality, generalized medicine including basic pediatric, adult and geriatric wellness, dentistry, and surgery.</a></li>
							<li>We provide financial assistance / vet services for the medical care of pets whose owners are unable to afford the expense. </a></li>
<ul> </p>
